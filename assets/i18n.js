function t(key, ...args) {
  const lang = getLang();
  const val = TRANSLATIONS[lang]?.[key] ?? TRANSLATIONS['en']?.[key] ?? key;
  return typeof val === 'function' ? val(...args) : val;
}

function getLang() {
  return localStorage.getItem('wlpr_lang') || 'en';
}

function setLang(lang) {
  localStorage.setItem('wlpr_lang', lang);
}
